    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if(lbSearch.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Insira um valor no campo de pesquisa");
        } else {
            String searchForm = (String) cbSearchForm.getSelectedItem();
            String textForm = null;
            
            switch (searchForm) {
                case "Nome":
                    textForm = "studantName";
                    break;
                case "Telefone":
                    textForm = "studantPhone";
                    break;
                case "Email":
                    textForm = "studantEmail";
                    break;
                case "Cidade":
                    textForm = "studantCity";
                    break;
                case "Estado":
                    textForm = "studantState";
                    break;
            }
            
            setTable(textForm, lbSearch.getText());
        }
    }//GEN-LAST:event_btnSearchActionPerformed

